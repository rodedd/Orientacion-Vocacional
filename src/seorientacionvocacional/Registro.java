    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        ConexionMySQL cc = new ConexionMySQL();
        Connection cn = cc.Conectar();
        String usuario="", pass="", nombre="", paterno="", materno="", correo="";
        String sql = "";
        
        usuario = txtUsuario.getText();
        pass = new String (txtPass.getPassword());
        nombre = txtNombre.getText();
        paterno = txtPaterno.getText();
        materno = txtMaterno.getText();
        correo = txtCorreo.getText();
        
        //usuario.replaceAll(" ","");
        //pass.replaceAll(" ","");
        //if(usuario.length()==0 || pass.length()==0)
        if(usuario.isEmpty() || pass.isEmpty())
        {
            //JOptionPane.showMessageDialog(null, "No pueden quedar campos vacios");
            JOptionPane.showMessageDialog(this, "Existe Algun Campo Vac√≠o", "Error!", JOptionPane.ERROR_MESSAGE);
        }
        else{
            
        sql = "INSERT INTO usuarios (usuario, clave, nombre, ap_paterno, ap_materno, correo) VALUES (?,?,?,?,?,?)";
        try {
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1,usuario);
            pst.setString(2,pass);
            pst.setString(3,nombre);
            pst.setString(4,paterno);
            pst.setString(5,materno);
            pst.setString(6,correo);
            int n = pst.executeUpdate();
            if(n>0)
            {
                JOptionPane.showMessageDialog(null,"Registro Con Exito");
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        this.setVisible(false);
        AccesoUsuario vAcc = new AccesoUsuario();
        vAcc.setVisible(true);
        vAcc.pack();
        
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

